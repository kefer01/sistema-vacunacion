        <h3>Que es una pandemia</h3>
        <p>
            Una pandemia ​ es una epidemia de una enfermedad infecciosa que se ha propagado en un área geográficamente extensa, por ejemplo, en varios continentes o en todo el mundo, afectando a un número considerable de personas.
        </p>
    </div>
    <div class="card">
        <h3>¿Existe cura?</h3>
        <p>
            La enfermedad por coronavirus (COVID‑19) es una enfermedad infecciosa provocada por el virus SARS-CoV-2.
            La mayoría de las personas que padecen COVID‑19 sufren síntomas de intensidad leve a moderada y se recuperan sin necesidad de tratamientos especiales. Sin embargo, algunas personas desarrollan casos graves y necesitan atención médica.
        </p>
    </div>
    <div class="card">
        <h3>Que sucedera ahora</h3>
        <p>
            Las zonas urbanas son la zona cero de la pandemia del COVID-19, con un 90 % de los casos comunicados.
            Las ciudades están sufriendo las peores consecuencias de la crisis, muchas de ellas con sistemas de salud sobrecargados, servicios de agua y saneamiento inadecuados y otros problemas.
            Este es el caso en particular en las zonas más pobres, donde la pandemia ha puesto de manifiesto desigualdades profundamente arraigadas.
        </p>
    </div>
